package core;

public class Strega {

	private int posx,posy;
	
	public Strega()
	{
		posx=50;
		posy=2000;
	}

	public int getPosx() {
		return posx;
	}

	public void setPosx(int posx) {
		this.posx = posx;
	}

	public int getPosy() {
		return posy;
	}

	public void setPosy(int posy) {
		this.posy = posy;
	}
	
	
	
}
